    int LTimerStart(lua_State* io_luaState)
    {
        _geti(1, timer);
        _getf(2, time);
        _getb(3,periodic);
        if (!lua_isfunction(io_luaState, 4))
        {
            return luaL_error(io_luaState, "function %s Argument#%d should be of function type", __func__, 4);
        }
        
        //DWORD callback = (DWORD)pcallback;
        //mlog::Debug("Starting timer with info: ", timer, " ", time, " ", periodic," ", IntToHex(callback), " jassid: ",pcallback->jassid);
        TimerStart(timer, time, periodic, AddCallbackHandled(io_luaState,timer));
        return 0;
    }

    int LDestroyTimer(lua_State* io_luaState)
    {
        _geti(1, timer);
        DestroyTimer(timer);
        RemoveRefsFromHandle(timer);
        return 0;
    }

    int LTestAddCallback(lua_State* io_luaState)
    {
        
        if (!lua_isfunction(io_luaState, -1))
        {
            return luaL_error(io_luaState, "function %s Argument#%d should be of function type", __func__, 1);
        }
        int id = jcallbackcode::Ccalbacks.AddCallback(io_luaState);
        jcallbackcode::JCallback* pcallback = jcallbackcode::Ccalbacks.callbacks[id];
        DWORD callback = (DWORD)pcallback;
        mlog::Debug("Starting timer with info: ", IntToHex(callback), " jassid: ", pcallback->jassid);
        return 0;
    }
