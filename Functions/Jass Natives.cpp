{
	char* cToCopy = new char[5];
	cToCopy[0] = reinterpret_cast<char*>(&num)[3];
	cToCopy[1] = reinterpret_cast<char*>(&num)[2];
	cToCopy[2] = reinterpret_cast<char*>(&num)[1];
	cToCopy[3] = reinterpret_cast<char*>(&num)[0];
	cToCopy[4] = 0;

	return cToCopy;
}

